if ( ! function_exists( 'bakery_vc_before_init' ) ) {
	function bakery_vc_before_init() {
		if (function_exists( 'vc_manager' ) ) {
			vc_manager()->setAsNetworkPlugin( true );
		}
		
		if ( function_exists( 'vc_set_as_theme' ) ){
			vc_set_as_theme( true );
		}
	}

	add_action( 'init', 'bakery_vc_before_init' );
}

// VC Assets Register
if ( ! function_exists( 'bakery_vc_assets_register' ) ) {
	function bakery_vc_assets_register() {
		// Select2
		wp_register_style( 'bakery-vc-assets-select2', BAKERY_THEME_URL . 'includes/vc-addons/assets/lib/select2/css/select2.min.css', array(), '4.0.3' );
		wp_register_script( 'bakery-vc-assets-select2', BAKERY_THEME_URL . 'includes/vc-addons/assets/lib/select2/js/select2.full.min.js', array( 'jquery' ), '4.0.3', true );

		// Style
		wp_register_style( 'bakery-vc-assets-css', BAKERY_THEME_URL . 'includes/vc-addons/assets/css/vc-style.css', array(), BAKERY_THEME_VERSION );

		// Icon Picker
		wp_register_script( 'bakery-vc-assets-iconpicker', BAKERY_THEME_URL . 'includes/vc-addons/assets/js/iconpicker.min.js', array( 'jquery' ), '1.2.2', true );

		// Script
		wp_register_script( 'bakery-vc-assets-js', BAKERY_THEME_URL . 'includes/vc-addons/assets/js/vc-script.js', array( 'jquery' ), BAKERY_THEME_VERSION, true );
	}
}

add_action( 'init', 'bakery_vc_assets_register' );

// VC Assets Enqueue
if ( ! function_exists( 'bakery_vc_assets_enqueue' ) ) {
	function bakery_vc_assets_enqueue() {
		// Styles
		wp_enqueue_style(
			array(
				'bakery-vc-assets-css',
				'bakery-vc-assets-select2'
			)
		);

		// Scripts
		wp_enqueue_script( 
			array(
				'bakery-vc-assets-js',
				'bakery-vc-assets-iconpicker',
				'bakery-vc-assets-select2'
			)
		);
	}
}

add_action( 'admin_enqueue_scripts', 'bakery_vc_assets_enqueue' );

